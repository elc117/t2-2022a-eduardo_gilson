tem_filhos_com(X,Y) :-
parent(X,F), parent(Y,F), Y \== X.


filho_de(X,Y) :-
	parent(X, Y).

filhos_de(X, Filhos) :-
	setof(Y, parent(X,Y), Filhos),
	!.

filhos_de(X, Filhos) :-
	not(setof(Y, parent(X,Y), Filhos)),		
	Filhos = unknown.	

pai_de(X, Y) :-
    parent(Y, X).

pais_de(X, Pais) :-
	setof(Y, parent(Y, X), Pais),
	!.

pais_de(X, Pais) :-
	not(setof(Y, parent(Y, X), Pais)),		
	Pais = unknown.

irmaos_a(X, Y) :- %vem duplicado
	parent(Z, X),
	parent(Z, Y),
	X \= Y.

lista_de_irmaos(X, Irmaos) :-
	setof(Y, irmaos_a(X,Y), Irmaos);	%Lista que exclui duplicados 
	Irmaos = none.

irmaos(X, Y) :- % funciona do jeito certo kkk
	lista_de_irmaos(X, Irmaos),
	member(Y, Irmaos).

ancestral(X, Y) :-
    parent(X, Y).

ancestral(X, Y) :-
    parent(X, Z),
    ancestral(Z, Y), 
    Y \== X.

ancestrais(X, Ancestral_de) :-
    findall(A, ancestral(X, A), Ancestral_de).

